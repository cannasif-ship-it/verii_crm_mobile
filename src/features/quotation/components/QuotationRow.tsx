import React, { memo } from "react";
import { View, TouchableOpacity, StyleSheet } from "react-native";
import { Text } from "../../../components/ui/text";
import { useUIStore } from "../../../store/ui";
import { useTranslation } from "react-i18next";
import type { QuotationGetDto } from "../types";

interface QuotationRowProps {
  quotation: QuotationGetDto;
  onPress: (id: number) => void;
  onRevision: (e: any, id: number) => void;
  isPending: boolean;
}

function formatDate(dateString: string | null | undefined, language: string): string {
  if (!dateString) return "-";
  return new Date(dateString).toLocaleDateString(language === "tr" ? "tr-TR" : "en-US");
}

function formatCurrency(amount: number, currencyCode: string, language: string): string {
  try {
    return new Intl.NumberFormat(language === "tr" ? "tr-TR" : "en-US", {
      style: "currency",
      currency: currencyCode,
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    }).format(amount);
  } catch {
    return (
      new Intl.NumberFormat(language === "tr" ? "tr-TR" : "en-US", {
        style: "decimal",
        minimumFractionDigits: 2,
        maximumFractionDigits: 2,
      }).format(amount) +
      " " +
      currencyCode
    );
  }
}

function QuotationRowComponent({
  quotation,
  onPress,
  onRevision,
  isPending,
}: QuotationRowProps): React.ReactElement {
  const { colors, themeMode } = useUIStore();
  const { i18n } = useTranslation();

  const handleRowPress = (): void => {
    onPress(quotation.id);
  };

  const handleRevisionPress = (e: any): void => {
    e.stopPropagation();
    onRevision(e, quotation.id);
  };

  const cardBackground = themeMode === "dark" ? "rgba(20, 10, 30, 0.7)" : colors.card;
  const statusColor = quotation.status === 1 ? colors.accent : colors.textMuted;
  const statusText = quotation.status === 1 ? "Aktif" : "Pasif";

  return (
    <TouchableOpacity
      style={[styles.container, { backgroundColor: cardBackground, borderColor: colors.cardBorder }]}
      onPress={handleRowPress}
      activeOpacity={0.7}
    >
      <View style={styles.content}>
        <View style={styles.header}>
          <View style={styles.headerLeft}>
            <Text style={[styles.id, { color: colors.text }]}>#{quotation.id}</Text>
            {quotation.offerNo && (
              <Text style={[styles.offerNo, { color: colors.textSecondary }]}>
                {quotation.offerNo}
              </Text>
            )}
          </View>
          <View style={[styles.statusBadge, { backgroundColor: colors.activeBackground }]}>
            <Text style={[styles.statusText, { color: statusColor }]}>{statusText}</Text>
          </View>
        </View>

        <View style={styles.details}>
          <View style={styles.detailRow}>
            <Text style={[styles.detailLabel, { color: colors.textMuted }]}>Müşteri:</Text>
            <Text style={[styles.detailValue, { color: colors.text }]} numberOfLines={1}>
              {quotation.potentialCustomerName || "-"}
            </Text>
          </View>

          {quotation.representativeName && (
            <View style={styles.detailRow}>
              <Text style={[styles.detailLabel, { color: colors.textMuted }]}>Temsilci:</Text>
              <Text style={[styles.detailValue, { color: colors.text }]} numberOfLines={1}>
                {quotation.representativeName}
              </Text>
            </View>
          )}

          <View style={styles.detailRow}>
            <Text style={[styles.detailLabel, { color: colors.textMuted }]}>Tarih:</Text>
            <Text style={[styles.detailValue, { color: colors.text }]}>
              {formatDate(quotation.offerDate, i18n.language)}
            </Text>
          </View>

          <View style={styles.detailRow}>
            <Text style={[styles.detailLabel, { color: colors.textMuted }]}>Para Birimi:</Text>
            <Text style={[styles.detailValue, { color: colors.text }]}>{quotation.currency}</Text>
          </View>

          <View style={styles.detailRow}>
            <Text style={[styles.detailLabel, { color: colors.textMuted }]}>Toplam:</Text>
            <Text style={[styles.totalValue, { color: colors.text }]}>
              {formatCurrency(quotation.grandTotal, quotation.currency, i18n.language)}
            </Text>
          </View>
        </View>

        <TouchableOpacity
          style={[styles.revisionButton, { borderColor: colors.border }]}
          onPress={handleRevisionPress}
          disabled={isPending}
          activeOpacity={0.7}
        >
          <Text style={[styles.revisionButtonText, { color: colors.text }]}>
            {isPending ? "Yükleniyor..." : "Revize Et"}
          </Text>
        </TouchableOpacity>
      </View>
    </TouchableOpacity>
  );
}

export const QuotationRow = memo(QuotationRowComponent);

const styles = StyleSheet.create({
  container: {
    padding: 16,
    borderRadius: 12,
    borderWidth: 1,
    marginBottom: 12,
  },
  content: {
    gap: 12,
  },
  header: {
    flexDirection: "row",
    justifyContent: "space-between",
    alignItems: "center",
  },
  headerLeft: {
    flexDirection: "row",
    alignItems: "center",
    gap: 8,
  },
  id: {
    fontSize: 16,
    fontWeight: "600",
  },
  offerNo: {
    fontSize: 14,
  },
  statusBadge: {
    paddingHorizontal: 8,
    paddingVertical: 4,
    borderRadius: 6,
  },
  statusText: {
    fontSize: 12,
    fontWeight: "500",
  },
  details: {
    gap: 6,
  },
  detailRow: {
    flexDirection: "row",
    alignItems: "center",
  },
  detailLabel: {
    fontSize: 13,
    marginRight: 8,
    minWidth: 90,
  },
  detailValue: {
    fontSize: 13,
    flex: 1,
  },
  totalValue: {
    fontSize: 14,
    fontWeight: "600",
    flex: 1,
  },
  revisionButton: {
    paddingVertical: 8,
    paddingHorizontal: 16,
    borderRadius: 8,
    borderWidth: 1,
    alignItems: "center",
    justifyContent: "center",
    marginTop: 4,
  },
  revisionButtonText: {
    fontSize: 14,
    fontWeight: "600",
  },
});
